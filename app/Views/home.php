                <h2>Ons Doel</h2>
                <p>De Lotgenotengroep Autisme Limburg heeft als doel om lotgenotencontact aan te bieden aan volwassenen met (een vermoeden van) een Autisme Spectrum Stoornis (ASS) in de provincie Limburg. Wij stellen geen eisen op het gebied van begaafdheid, maar verwachten wel dat deelnemers zich op hun gemak voelen in een groep en zich daarin redelijk kunnen uiten. Partners, ouders en begeleiders die iemand met ASS vergezellen zijn ook welkom. Dat maakt onze aanpak uniek.</p>
                <p>Met de term lotgenoten duiden we op mensen die in dezelfde situatie of levensomstandigheden verkeren, wat geldt voor mensen met autisme, aangezien die vaak dezelfde soort ervaringen hebben gehad in hun leven.</p>
                <p>Door het organiseren van minimaal één activiteit per maand en een, afhankelijk van de locatie, twee- of vierwekelijkse lotgenotenavond willen wij bereiken dat onze deelnemers:</p>
                <ul class="list-group list-group-numbered">
                    <li class="list-group-item list-group-item-info">Sociale contacten hebben en vriendschappen kunnen sluiten.</li>
                    <li class="list-group-item list-group-item-info">Meer stabiliteit hebben in hun dagelijks leven.</li>
                    <li class="list-group-item list-group-item-info">Zich lichamelijk en geestelijk beter voelen.</li>
                    <li class="list-group-item list-group-item-info">Minder afhankelijk zijn van professionele ondersteuning.</li>
                    <li class="list-group-item list-group-item-info">Een positieve kijk hebben op een leven met ASS en de kansen die er zijn.</li>
                    <li class="list-group-item list-group-item-info">Meer zelfvertrouwen hebben en beter communiceren.</li>
                </ul>

                <div class="post"></div>

                <h2 class="mt-3">Ons Bod</h2>
                <p>In een gezellige en ongedwongen sfeer kun je bij ons mensen ontmoeten die jou begrijpen en steunen. Om de drempel voor deelname laag te houden rekenen wij geen contributie. Ook ben je niet verplicht om naar bijeenkomsten te komen.</p>

                <p>Wij bieden een gestructureerde, rustige en veilige omgeving, waarin het aantal prikkels beperkt is.
                We vragen persoonlijke ervaringen niet naar buiten te brengen en te delen buiten de groep. Daarnaast vragen we van deelnemers dat ze na een tijd wat meer over zichzelf kunnen vertellen, over behoeftes, leuke en vervelende ervaringen.
                Wij vragen van begeleidende ouders of hulpverleners de persoon met ASS het woord te laten doen. Niet te snel zaken voor te zeggen, in te vullen of over te nemen.</p>

                <p>We zijn een organisatie voor mensen met autisme, door vrijwilligers met autisme.</p>

                <p>We werken samen met de Nederlandse Vereniging voor Autisme, verschillende instellingen voor Geestelijke Gezondheidszorg (GGZ) in Limburg en een aantal andere organisaties die mensen met ASS begeleiden. We zijn partner van het Autisme Netwerk Zuid-Limburg.</p>

                <div class="post"></div>

                <h2>Onze Waarde</h2>
                <p>Voor veel van onze deelnemers is het contact waardevol. Door het uitwisselen van ervaringen kom je meer te weten over autisme en hoe je er mee om kunt gaan. Bovendien kunnen andere ervaringen je steun geven. Je krijgt meer kennis en praktisch tips. Je kunt informatie checken.</p>

                <p>Het is fijn om steun en bemoediging te krijgen van lotgenoten in moeilijke tijden of als je voor lastige beslissingen staat. Ook voor jouw naasten kan het contact met andere naasten of lotgenoten belangrijk zijn. Ze krijgen meer informatie en kunnen jou beter begrijpen en steunen. En het kan hen helpen om beter met hun zorgen om te gaan.</p>

                <p>Lotgenotencontact speelt een belangrijke rol bij jouw empowerment en het vergroten van je eigen kracht</p>
                <ul class="list-group list-group-numbered">
                    <li class="list-group-item list-group-item-info">om de juiste hulpvraag te stellen en de juiste passende hulp te krijgen.</li>
                    <li class="list-group-item list-group-item-info">om beter met dagelijkse dingen om te gaan.</li>
                </ul>

