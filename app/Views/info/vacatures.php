        <h1>Vacatures</h1>

        <p>De Lotgenotengroep Autisme Limburg (LGAL) is dringend op zoek naar enthousiaste GESPREKSLEIDERS voor onze lotgenotenavonden.</p>

        <h4>Wie zoeken wij:</h4>
        <ul>
            <li>Mensen met autisme die het leuk vinden om de lotgenotenavonden te begeleiden.</li>
            <li>Mensen zonder autisme maar met kennis van en affiniteit met autisme die het leuk vinden om de lotgenotenavonden te begeleiden.</li>
            <li>Mensen die in staat zijn een lotgenotenavond te begeleiden, oftewel een groep van 8 tot 12 deelnemers begeleiden aan de hand van een vooraf uitgekozen onderwerp.</li>
            <li>Mensen die het als gespreksleider belangrijk vinden dat we elkaar met respect behandelen en de antwoorden en ervaringen van alle deelnemers als waardevol zien.</li>
            <li>Je herkent jezelf in een groot deel van de volgende eigenschappen:</li>
            <ul>
                <li>Je kunt goed luisteren.</li>
                <li>Je kunt samenvatten wat er gezegd is.</li>
                <li>Je hebt een open en nieuwsgierige houding.</li>
                <li>Je kunt neutraal blijven.</li>
                <li>Je weet hoe je kan reageren op emoties van anderen.</li>
                <li>Je kunt mensen bij de les houden.</li>
                <li>Je kunt de tijd bewaken en grenzen stellen.</li>
            </ul>
        </ul>

        <h4>Wat ga je doen:</h4>
        <ul>
            <li>1 keer per 2 of 4 weken ben je op de dinsdagavond gespreksleider bij de lotgenotengroep. Meestal in Brunssum, maar het zou fijn zijn als we je incidenteel kunnen vragen om dit in Maastricht te doen.</li>
            <li>Voor de lotgenotenavonden zorg je ervoor dat je jezelf hebt voorbereid op het thema voor die avond samen met je collega-gespreksleider.</li>
            <li>Tijdens de lotgenotenavonden zorg je ervoor dat je de tijd bewaakt en dat de deelnemers allemaal de mogelijkheid krijgen om hun verhaal te doen.</li>
            <li>Je oordeelt niet over de verhalen van de deelnemers en ziet erop toe dat de anderen dit ook niet doen.</li>
            <li>Een paar keer per jaar sluit je, ook op de dinsdagavond, aan bij vergaderingen.</li>
            <li>Wanneer je het leuk vindt, kan je deelnemen aan activiteiten die de LGAL organiseert. (bowlen, wandelen, enz.)</li>
        </ul>

        <h4>Wat krijg je ervoor terug:</h4>
        <ul>
            <li>Je ontmoet bijzondere en diverse volwassen mensen met autisme.</li>
            <li>Je bent een waardevolle toevoeging aan het leuke team van vrijwilligers, je hoort er meteen bij.</li>
            <li>Je bent een aanvulling in het welzijn van mensen met autisme.</li>
            <li>Je krijgt een reiskostenvergoeding.</li>
            <li>Je krijgt een goed gevoel doordat je je als vrijwilliger inzet voor je medemens.</li>
        </ul>
        
        <p>Als dit je aanspreekt, kom dan een keer kennismaken met een van de gespreksleiders. We vertellen je er graag meer over.</p>
