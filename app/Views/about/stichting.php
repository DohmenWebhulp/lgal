        <h2>ANBI-status</h2>
        <p>Sinds 1 januari 2017 heeft de stichting de <a href="https://www.belastingdienst.nl/wps/wcm/connect/bldcontentnl/belastingdienst/zakelijk/bijzondere_regelingen/goede_doelen/algemeen_nut_beogende_instellingen/wat_is_een_anbi" target="_blank">ANBI-status</a>.</p>

        <p>Een ANBI is een algemeen nut beogende instelling. Een instelling kan alleen een ANBI zijn, als ze zich voor minstens 90% inzet voor het algemeen nut.</p>

        <p>Een ANBI heeft een aantal belastingvoordelen:</p>

        <ul>
            <li>Een ANBI betaalt geen erfbelasting of schenkbelasting voor erfenissen en schenkingen die de instelling gebruikt voor het algemeen belang.</li>
            <li>Als een ANBI zelf schenkingen doet in het algemene belang, dan hoeft de ontvanger geen schenkbelasting te betalen.</li>
            <li>Een ANBI komt in aanmerking voor teruggaaf van energiebelasting.</li>
            <li>Vrijwilligers die voor een ANBI werken, doen daarmee onder bepaalde voorwaarden een gift aan een ANBI.</li>
            <li>Donateurs van een ANBI mogen hun giften aftrekken van de inkomsten- of vennootschapsbelasting.</li>
            <li>Om in aanmerking te komen voor de aftrek van periodieke giften, moeten de donateur en de ANBI de gift vastleggen in een overeenkomst.</li>
        </ul>

