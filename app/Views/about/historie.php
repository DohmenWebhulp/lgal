            <p>Omdat het voor mensen met autisme vaak moeilijk is om met andere mensen contact te leggen en/of te onderhouden, heeft Mannolito Schuivens († 26-10-2015) in 2012 het initiatief genomen om de lotgenotengroep op te richten. In eerste instantie werden er alleen activiteiten georganiseerd. In 2014 kwamen daar de tweewekelijkse lotgenotenavonden bij. Oorspronkelijk werd de lotgenotenavond “inloopavond” genoemd. Die term wekte echter soms verwarring.</p>

            <p>Omdat de groep steeds groter werd, werd het voor Mannolito moeilijker om de organisatie alleen op zijn schouders te nemen. Sinds het najaar van 2014 wordt de groep aangestuurd door een commissie. Deze komt elke drie maanden bij elkaar. Onder die commissie vallen drie subcommissies: Lotgenotenavond, Communicatie en Activiteiten. Sinds 3 maart 2016 zijn wij een <a href="stichting">stichting</a>.</p>

            <p>Soms krijgen wij als commissie wel eens de vraag “waarom heet de groep eigenlijk lotgenotengroep en niet contactgroep of zoiets?” De belangrijkste reden om de groep zo te blijven noemen is dat onze oprichter, Mannolito Schuivens, de groep zo heeft genoemd. Dat willen wij in ere blijven houden. En als je kijkt naar een definitie van lotgenotencontact “Contact tussen mensen (of tussen ouders van kinderen) met een bepaalde beperking, aandoening of problematiek, ter ondersteuning van elkaar”, dan zie je dat de naam eigenlijk heel goed gekozen is.

            <p>Mannolito zal in onze gedachten voort blijven bestaan. We zijn hem zeer dankbaar voor het oprichten van onze mooie groep.</p>
